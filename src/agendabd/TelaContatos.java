     private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        if(usuario.getAcesso() < 2)
            JOptionPane.showMessageDialog(null, "Erro:Acesso indevido - > Necessario nivel superior para Alterar informações.");
        else{
            if (jTContatos.getSelectedRow() != -1) {
                if(!txtNome.getText().equals("") && !txtNumero.getText().equals("")){
                    Contato p = new Contato();
                    ContatoDAO dao = new ContatoDAO();

                    p.setNome(txtNome.getText());
                    p.setNumero(Integer.parseInt(txtNumero.getText()));
                    p.setEmail(txtEmail.getText());
                    dao.update(usuario,p);

                    txtNome.setText("");
                    txtEmail.setText("");
                    txtNumero.setText("");

                    readJTable();  
                }
                else{
                    JOptionPane.showMessageDialog(null, "Insira as informações corretamente!");
                }                
            }
        }


    }//GEN-LAST:event_jButton3ActionPerformed

