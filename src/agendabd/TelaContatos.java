    public void readJTable() {
        
        DefaultTableModel modelo = (DefaultTableModel) jTContatos.getModel();
        modelo.setNumRows(0);
        ContatoDAO agenda = new ContatoDAO();

        for (Contato c : agenda.read(usuario)) {

            modelo.addRow(new Object[]{
                c.getNome(),
                c.getEmail(),
                c.getNumero()
            });

        }

    }

