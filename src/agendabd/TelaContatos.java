    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(!txtNome.getText().equals("") && !txtNumero.getText().equals("")){
            Contato c = new Contato();
            ContatoDAO dao = new ContatoDAO();

            c.setNome(txtNome.getText());
            c.setEmail(txtEmail.getText());
            c.setNumero(Integer.parseInt(txtNumero.getText()));
            dao.create(usuario,c);

            txtNome.setText("");
            txtEmail.setText("");
            txtNumero.setText("");

            readJTable();
        }else
           JOptionPane.showMessageDialog(null, "Insira as informações antes de cadastrar!"); 
        

}//GEN-LAST:event_jButton1ActionPerformed
    
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

//        System.out.println("Linha selecionada: "+jTProdutos.getSelectedRow());
        if(usuario.getAcesso() != 3)
            JOptionPane.showMessageDialog(null, "Erro:Acesso indevido - > Necessario privilegio de Administrador para Excluir informações.");
        else{
            if (jTContatos.getSelectedRow() != -1) {

                Contato p = new Contato();
                ContatoDAO dao = new ContatoDAO();

                p.setNome((String) jTContatos.getValueAt(jTContatos.getSelectedRow(), 0));

                dao.delete(usuario,p);

                txtNome.setText("");
                txtEmail.setText("");
                txtNumero.setText("");

                readJTable();

            } else {
                JOptionPane.showMessageDialog(null, "Selecione um contato para excluir.");
            }
        }


    }//GEN-LAST:event_jButton2ActionPerformed
    
